			  <div class="tab-pane fade subtext" id="profile" role="tabpanel" aria-labelledby="profile-tab">Cara Menggunakan<br>
				Hal pertama yang harus dilakukan sebelum memasang IUD adalah konsultasi ke Dokter Kandungan atau Bidan. Mereka akan menanyakan berbagai pertanyaan tentang riwayat kesehatan dan gaya hidup kamu untuk memastikan bahwa kamu adalah kandidat yang tepat untuk menggunakan IUD.<br><br>

				Kemudian, Dokter atau Bidan akan memastikan kondisi kesehatanmu dan memasang IUD di rahim pada saat menstruari hari ketiga (idealnya), karena pada saat menstruasi rahim memiliki kondisi yang paling terbuka, sehingga mudah dipasang.<br><br>

				Sangat umum untuk merasakan sedikit kram ketika IUD dimasukkan ke rahim, tetapi Dokter atau Bidan akan memberikan resep obat pereda nyeri. Setelah IUD berada di rahim, akan ada tali kecil (string) yang menggantung di dalam vagina yang berguna untuk memudahkan pelepasan IUD.<br><br>

