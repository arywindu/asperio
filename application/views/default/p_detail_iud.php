				Para dokter kandungan setuju bahwa mereka yang cocok memakai IUD adalah yang memiliki rahim dengan kondisi sehat. Bahkan, IUD direkomendasikan untuk digunakan pada perempuan muda dan belum pernah hamil. IUD juga tidak akan mempengaruhi kesuburan, dan pemakai IUD dapat langsung hamil kembali sesaat setelah melepaskan IUD-nya.
