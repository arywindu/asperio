				<br><br>
				IUD cocok bagi orang yang kurang disiplin dan sering lupa memakai kontrasepsi untuk mencegah kehamilan, karena perlindungannya lama bahkan hingga 10 tahun. Tergantung IUD mana yang ingin dipakai.
